CREATE TABLE IDOL_NAME (MEMBER_NAME VARCHAR2(100), REAL_NAME VARCHAR2(100));
CREATE TABLE IDOL_BIRTHDAY (MEMBER_NAME VARCHAR2(100), BIRTHDAY VARCHAR2(8));
CREATE TABLE IDOL_SNS (MEMBER_NAME VARCHAR2(100), SNS_INFO VARCHAR2(100));

SELECT * FROM IDOL_MEMBER;

-- IDOL_MEMBER 테이블에서 BTS 멤버들의 정보가 IDOL_NAME, IDOL_BIRTHDAY 테이블로 들어감
INSERT ALL
INTO IDOL_NAME VALUES (MEMBER_NAME, REAL_NAME)
INTO IDOL_BIRTHDAY VALUES (MEMBER_NAME, BIRTHDAY)
SELECT * FROM IDOL_MEMBER WHERE GROUP_NAME = 'BTS';

SELECT * FROM IDOL_NAME;
SELECT * FROM IDOL_BIRTHDAY;

INSERT ALL
WHEN LENGTH(GROUP_NAME) < 4 THEN INTO IDOL_NAME VALUES (MEMBER_NAME, REAL_NAME) -- 그룹 이름이 4글자 미만일 경우 IDOL_NAME 테이블에 입력
WHEN LENGTH(GROUP_NAME) < 5 THEN INTO IDOL_BIRTHDAY VALUES (MEMBER_NAME, BIRTHDAY) -- 그룹 이름이 5글자 미만일 경우 IDOL_BIRTHDAY 테이블에 입력
ELSE INTO IDOL_SNS VALUES (MEMBER_NAME, SNS_INFO) -- 위 2행에 포함되지 않는 아이돌들 IDOL_SNS 테이블에 입력
SELECT * FROM IDOL_MEMBER;

SELECT * FROM IDOL_NAME; -- BTS
SELECT * FROM IDOL_BIRTHDAY; -- BTS, 트와이스, 아스트로
SELECT * FROM IDOL_SNS; -- (여자)아이들

-- INSERT FIRST는 INSERT ALL과 다르게 위의 조건을 만족하면 아래 조건을 스킵 (IF ELSE 문과 비슷)
INSERT FIRST
WHEN LENGTH(GROUP_NAME) < 4 THEN INTO IDOL_NAME VALUES (MEMBER_NAME, REAL_NAME) -- 그룹 이름이 4글자 미만일 경우 IDOL_NAME 테이블에 입력
WHEN LENGTH(GROUP_NAME) < 5 THEN INTO IDOL_BIRTHDAY VALUES (MEMBER_NAME, BIRTHDAY) -- 그룹 이름이 5글자 미만일 경우 IDOL_BIRTHDAY 테이블에 입력
ELSE INTO IDOL_SNS VALUES (MEMBER_NAME, SNS_INFO) -- 위 2행에 포함되지 않는 아이돌들 IDOL_SNS 테이블에 입력
SELECT * FROM IDOL_MEMBER;

